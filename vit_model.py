def train(train_images_path, train_images_label, val_images_path=None, val_images_label=None, classes=2, epochs=20, batch_size=32,
          lr=0.001, lrf=0.01, weight='./pretrained/vit_base_patch16_224_in21k.pth', freeze=False, device='cuda:0', val=True, data=1, init=True):
    device = torch.device(device if torch.cuda.is_available() else "cpu")
    if val_images_label is None:
        val_images_label = []
    if val_images_path is None:
        val_images_path = []
    data_transform = {
        "train": transforms.Compose([transforms.RandomResizedCrop(224),
                                     transforms.RandomHorizontalFlip(),
                                     transforms.ToTensor(),
                                     transforms.Normalize([0.5, 0.5, 0.5], [0.5, 0.5, 0.5])]),
        "val": transforms.Compose([transforms.Resize(256),
                                   transforms.CenterCrop(224),
                                   transforms.ToTensor(),
                                   transforms.Normalize([0.5, 0.5, 0.5], [0.5, 0.5, 0.5])])}

    train_dataset = MyDataSet(images_path=train_images_path,
                              images_class=train_images_label,
                              transform=data_transform["train"])
    if val:
        val_dataset = MyDataSet(images_path=val_images_path,
                                images_class=val_images_label,
                                transform=data_transform["val"])

    nw = min([os.cpu_count(), batch_size if batch_size > 1 else 0, 8])
    print('Using {} dataloader workers every process'.format(nw))
    train_loader = torch.utils.data.DataLoader(train_dataset,
                                               batch_size=batch_size,
                                               shuffle=True,
                                               pin_memory=True,
                                               num_workers=nw,
                                               collate_fn=train_dataset.collate_fn)
    if val:
        val_loader = torch.utils.data.DataLoader(val_dataset,
                                                 batch_size=batch_size,
                                                 shuffle=False,
                                                 pin_memory=True,
                                                 num_workers=nw,
                                                 collate_fn=val_dataset.collate_fn)

    model = vit_base_patch16_224_in21k(num_classes=classes, has_logits=False).to(device)
    if init:
        assert os.path.exists(weight), "weights file: '{}' not exist.".format(weight)
        weights_dict = torch.load(weight, map_location=device)
        del_keys = ['head.weight', 'head.bias'] if model.has_logits \
            else ['pre_logits.fc.weight', 'pre_logits.fc.bias', 'head.weight', 'head.bias']
        for k in del_keys:
            del weights_dict[k]
        print(model.load_state_dict(weights_dict, strict=False))

    if freeze:
        for name, para in model.named_parameters():
            if "head" not in name and "pre_logits" not in name:
                para.requires_grad_(False)
            else:
                print("training {}".format(name))

    pg = [p for p in model.parameters() if p.requires_grad]
    optimizer = optim.SGD(pg, lr=lr, momentum=0.9, weight_decay=5E-5)
    lf = lambda x: ((1 + math.cos(x * math.pi / epochs)) / 2) * (1 - lrf) + lrf  # cosine
    scheduler = lr_scheduler.LambdaLR(optimizer, lr_lambda=lf)
    best_acc = 0.0
    best_epoch = 0
    for epoch in range(epochs):
        train_loss, train_acc = train_one_epoch(model=model,
                                                optimizer=optimizer,
                                                data_loader=train_loader,
                                                device=device,
                                                epoch=epoch)

        scheduler.step()
        val_loss, val_acc = 0, 0
        if val:
            val_loss, val_acc = evaluate(model=model,
                                         data_loader=val_loader,
                                         device=device,
                                         epoch=epoch)

        f = open('./res_dir/train_res/data'+str(data)+'/vit_res.txt', 'a')
        f.write('epoch: ' + str(epoch + 1) + '\n')
        f.write("train_loss: " + str(round(train_loss, 4)) + '\n')
        f.write("train_acc: " + str(round(train_acc, 4)) + '\n')
        f.write("val_loss: " + str(round(val_loss, 4)) + '\n')
        f.write("val_acc: " + str(round(val_acc, 4)) + '\n\n')
        f.close()
        if best_acc < train_acc:
            best_acc = train_acc
            best_epoch = epoch
            torch.save(model.state_dict(), './res_dir/weights/data'+str(data)+'/best_vit_model.pth')
    f1 = open('./res_dir/best_res/best.txt', 'a')
    f1.write('vit ' + 'epoch ' + str(best_epoch) + '  ' + str(best_acc) + '\n')


def predict(images_path, images_label, num_class=2, batch_size=8, data=1):
    device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")

    data_transform = transforms.Compose(
        [transforms.Resize(256),
         transforms.CenterCrop(224),
         transforms.ToTensor(),
         transforms.Normalize([0.5, 0.5, 0.5], [0.5, 0.5, 0.5])])
    nw = min([os.cpu_count(), batch_size if batch_size > 1 else 0, 8])
    dataset = MyDataSet(images_path=images_path,
                        images_class=images_label,
                        transform=data_transform)
    loader = torch.utils.data.DataLoader(dataset,
                                         batch_size=batch_size,
                                         shuffle=False,
                                         pin_memory=True,
                                         num_workers=nw,
                                         collate_fn=dataset.collate_fn)
    model_weight_path = './res_dir/weights/data'+str(data)+'/best_vit_model.pth'
    model = vit_base_patch16_224_in21k(num_classes=num_class, has_logits=False).to(device)
    model.load_state_dict(torch.load(model_weight_path, map_location=device))
    device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")
    score = cal_m(model=model, data_loader=loader, device=device, alo='vit', num=num_class)
    pd.DataFrame(np.array(score)).to_csv('./res_dir/label/data' + str(data) + '/vit' + '.csv', header=None, index=None)
    return score
